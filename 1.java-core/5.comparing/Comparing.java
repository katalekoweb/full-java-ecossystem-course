class Comparing 
{
    public static void main (String args[])
    {
        int x = 7;
        int y = 5;
        int a = 5;
        int b = 9;

        /**
         * Relational Operators
         * ==       -> equal
         * <        -> less than
         * >        -> greater than
         * !=       -> not equal
         * <=       -> less or equal
         * >=       -> greater or equal
         * 
         * Logical Operators
         * &&       -> and
         * ||       -> or
         * !        -> not
         */

        boolean result = (a < b) && (x > y);
        System.out.println(result);
    }
}